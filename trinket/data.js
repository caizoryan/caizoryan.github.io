// get text blocks from a channel
let channel = new Arena("trinket-css/");
channel.texts().then((res) => {
  for (const x of res) addStyle(x.content);
});

// for each text box run a add styles where arena content is the style
function addStyle(str) {
  let styleSheet = document.createElement("style");
  styleSheet.innerText = str;
  document.head.appendChild(styleSheet);
}

